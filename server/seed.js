const MONGODB_URI = "YOUR_MONGODB_CONNECTION_STRING";

async function seedDatabase() {
  try {
    // Connect to MongoDB
    await mongoose.connect(MONGODB_URI, {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });
    console.log('Connected to MongoDB');

    // Clear existing questions
    await Question.deleteMany({});
    console.log('Cleared existing questions');

    // Insert initial questions
    const questionDocuments = initialQuestions.map(text => ({ text }));
    await Question.insertMany(questionDocuments);
    console.log(`Successfully seeded ${initialQuestions.length} questions`);

    // Disconnect from MongoDB
    await mongoose.disconnect();
    console.log('Disconnected from MongoDB');
  } catch (error) {
    console.error('Error seeding database:', error);
    process.exit(1);
  }
}

